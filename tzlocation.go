package tzlocation

import "time"

type TZLocation string

func (t TZLocation) Load() (*time.Location, error) {
	return time.LoadLocation(string(t))
}

func (t TZLocation) LoadFromTZData(data []byte) (*time.Location, error) {
	return time.LoadLocationFromTZData(string(t), data)
}

